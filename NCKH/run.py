# 7. TOP-K EVALUATION
# =========================
def evaluate_topk(artifacts, test_df, k=10, max_users=100):
    model = artifacts["model"]
    products_df = artifacts["products_df"]
    inter_df = artifacts["inter_df"]
    meta = artifacts["meta"]

    user_encoder = meta["user_encoder"]
    item_encoder = meta["item_encoder"]
    brand_encoder = meta["brand_encoder"]
    platform_encoder = meta["platform_encoder"]
    scaler = meta["scaler"]
    numeric_cols = meta["numeric_cols"]

    user_positive_test = (
        test_df[test_df["interaction"] == 1]
        .groupby("user_id")["item_index"]
        .apply(set)
        .to_dict()
    )

    all_users = list(user_positive_test.keys())
    if len(all_users) == 0:
        return {
            "precision_at_k": 0.0,
            "recall_at_k": 0.0,
            "hit_rate_at_k": 0.0,
            "evaluated_users": 0
        }

    if len(all_users) > max_users:
        rng = np.random.RandomState(SEED)
        all_users = list(rng.choice(all_users, size=max_users, replace=False))

    precisions = []
    recalls = []
    hits = []

    known_items = set(item_encoder.classes_)

    for user_id in all_users:
        true_items = user_positive_test.get(user_id, set())
        if len(true_items) == 0:
            continue

        seen_items = set(inter_df.loc[inter_df["user_id"] == user_id, "item_index"].tolist())

        candidate_df = products_df[products_df["item_index"].isin(known_items)].copy()
        candidate_df = candidate_df[~candidate_df["item_index"].isin(seen_items)].copy()

        add_back_df = products_df[products_df["item_index"].isin(true_items)].copy()
        candidate_df = pd.concat([candidate_df, add_back_df], ignore_index=True)
        candidate_df = candidate_df.drop_duplicates(subset=["item_index"]).copy()

        if candidate_df.empty:
            continue

        try:
            user_encoded = user_encoder.transform([user_id])[0]
        except Exception:
            continue

        candidate_df["user_encoded"] = user_encoded
        candidate_df["item_encoded"] = item_encoder.transform(candidate_df["item_index"])

        candidate_df["brand_safe"] = candidate_df["brand"].astype(str).apply(
            lambda x: x if x in brand_encoder.classes_ else brand_encoder.classes_[0]
        )
        candidate_df["platform_safe"] = candidate_df["platform"].astype(str).apply(
            lambda x: x if x in platform_encoder.classes_ else platform_encoder.classes_[0]
        )

        candidate_df["brand_encoded"] = brand_encoder.transform(candidate_df["brand_safe"])
        candidate_df["platform_encoded"] = platform_encoder.transform(candidate_df["platform_safe"])

        candidate_df[numeric_cols] = scaler.transform(candidate_df[numeric_cols])

        inputs = {
            "user_input": candidate_df["user_encoded"].values,
            "item_input": candidate_df["item_encoded"].values,
            "brand_input": candidate_df["brand_encoded"].values,
            "platform_input": candidate_df["platform_encoded"].values,
            "numeric_input": candidate_df[numeric_cols].values.astype("float32")
        }

        scores = model.predict(inputs, verbose=0).ravel()
        candidate_df["score"] = scores

        topk_items = set(
            candidate_df.sort_values("score", ascending=False)
            .head(k)["item_index"]
            .tolist()
        )

        hit_count = len(topk_items & true_items)

        precision = hit_count / k
        recall = hit_count / len(true_items)
        hit = 1.0 if hit_count > 0 else 0.0

        precisions.append(precision)
        recalls.append(recall)
        hits.append(hit)

    if len(precisions) == 0:
        return {
            "precision_at_k": 0.0,
            "recall_at_k": 0.0,
            "hit_rate_at_k": 0.0,
            "evaluated_users": 0
        }

    return {
        "precision_at_k": float(np.mean(precisions)),
        "recall_at_k": float(np.mean(recalls)),
        "hit_rate_at_k": float(np.mean(hits)),
        "evaluated_users": int(len(precisions))
    }


