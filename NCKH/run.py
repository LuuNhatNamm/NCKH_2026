def preprocess_users(users_df):
    users_df = users_df.copy()
    users_df.columns = users_df.columns.str.strip()

    if "user_id" not in users_df.columns:
        raise ValueError("synthetic_users.csv phải có cột user_id")

    if "user_name" not in users_df.columns:
        users_df["user_name"] = users_df["user_id"].apply(lambda x: f"user_{x}")

    return users_df


def preprocess_interactions(inter_df):
    inter_df = inter_df.copy()
    inter_df.columns = inter_df.columns.str.strip()

    if "item_index" not in inter_df.columns:
        if "item_id" in inter_df.columns:
            inter_df = inter_df.rename(columns={"item_id": "item_index"})
        else:
            raise ValueError("File interaction phải có cột item_index hoặc item_id")

    required_cols = ["user_id", "item_index", "interaction"]
    for col in required_cols:
        if col not in inter_df.columns:
            raise ValueError(f"File interaction thiếu cột: {col}")

    inter_df["user_id"] = pd.to_numeric(inter_df["user_id"], errors="coerce")
    inter_df["item_index"] = pd.to_numeric(inter_df["item_index"], errors="coerce")
    inter_df["interaction"] = pd.to_numeric(inter_df["interaction"], errors="coerce")

    inter_df = inter_df.dropna(subset=["user_id", "item_index", "interaction"]).copy()

    inter_df["user_id"] = inter_df["user_id"].astype(int)
    inter_df["item_index"] = inter_df["item_index"].astype(int)
    inter_df["interaction"] = inter_df["interaction"].astype(int)

    return inter_df


