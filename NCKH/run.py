def recommend_for_user(user_id, artifacts, top_k=10, exclude_seen=True):
    model = artifacts["model"]
    products_df = artifacts["products_df"]
    data_encoded = artifacts["data_encoded"]
    inter_df = artifacts["inter_df"]
    meta = artifacts["meta"]

    user_encoder = meta["user_encoder"]
    item_encoder = meta["item_encoder"]
    brand_encoder = meta["brand_encoder"]
    platform_encoder = meta["platform_encoder"]
    scaler = meta["scaler"]
    numeric_cols = meta["numeric_cols"]

    if user_id not in set(data_encoded["user_id"].unique()):
        return pd.DataFrame()

    user_encoded = user_encoder.transform([user_id])[0]
    candidate_df = products_df.copy()

    if exclude_seen:
        seen_items = set(inter_df.loc[inter_df["user_id"] == user_id, "item_index"].tolist())
        candidate_df = candidate_df[~candidate_df["item_index"].isin(seen_items)].copy()

    known_items = set(item_encoder.classes_)
    candidate_df = candidate_df[candidate_df["item_index"].isin(known_items)].copy()

    if candidate_df.empty:
        return pd.DataFrame()

    # --- PHẦN SỬA LỖI Ở ĐÂY ---
    # 1. Tạo bản sao để Scale (dùng cho model), KHÔNG ghi đè lên candidate_df
    numeric_scaled = scaler.transform(candidate_df[numeric_cols])
    
    candidate_df["user_encoded"] = user_encoded
    candidate_df["item_encoded"] = item_encoder.transform(candidate_df["item_index"])
    
    # Xử lý brand/platform an toàn
    candidate_df["brand_safe"] = candidate_df["brand"].astype(str).apply(
        lambda x: x if x in brand_encoder.classes_ else brand_encoder.classes_[0]
    )
    candidate_df["platform_safe"] = candidate_df["platform"].astype(str).apply(
        lambda x: x if x in platform_encoder.classes_ else platform_encoder.classes_[0]
    )
    
    candidate_df["brand_encoded"] = brand_encoder.transform(candidate_df["brand_safe"])
    candidate_df["platform_encoded"] = platform_encoder.transform(candidate_df["platform_safe"])

    # 2. Truyền numeric_scaled (dữ liệu 0-1) vào model thay vì candidate_df[numeric_cols]
    inputs = {
        "user_input": candidate_df["user_encoded"].values,
        "item_input": candidate_df["item_encoded"].values,
        "brand_input": candidate_df["brand_encoded"].values,
        "platform_input": candidate_df["platform_encoded"].values,
        "numeric_input": numeric_scaled.astype("float32") # Dùng bản đã scale
    }

    scores = model.predict(inputs, verbose=0).ravel()
    candidate_df["score"] = scores

    # 3. Kết quả trả về lúc này vẫn giữ nguyên Price, Rating gốc (chưa bị scale)
    result_cols = [
        "item_index", "name", "brand", "price", "rating",
        "review_count", "discount_value", "platform", "seller",
        "product_url", "image_url", "score"
    ]

    recs = candidate_df.sort_values("score", ascending=False).head(top_k)[result_cols].copy()
    return recs.reset_index(drop=True)


def get_user_history(user_id, artifacts, top_n=10):
    inter_df = artifacts["inter_df"]
    products_df = artifacts["products_df"]

    history_df = inter_df[inter_df["user_id"] == user_id].copy()
    history_df = history_df[history_df["interaction"] == 1]

    history_df = history_df.merge(products_df, on="item_index", how="left")
    history_df = history_df[[
        "item_index", "name", "brand", "price", "rating",
        "review_count", "platform", "seller"
    ]].head(top_n)

    return history_df.reset_index(drop=True)


