def build_model(num_users, num_items, num_brands, num_platforms, embed_dim=32):
    user_input = tf.keras.layers.Input(shape=(1,), name="user_input")
    item_input = tf.keras.layers.Input(shape=(1,), name="item_input")
    brand_input = tf.keras.layers.Input(shape=(1,), name="brand_input")
    platform_input = tf.keras.layers.Input(shape=(1,), name="platform_input")
    numeric_input = tf.keras.layers.Input(shape=(4,), name="numeric_input")

    user_emb = tf.keras.layers.Embedding(num_users, embed_dim)(user_input)
    item_emb = tf.keras.layers.Embedding(num_items, embed_dim)(item_input)
    brand_emb = tf.keras.layers.Embedding(num_brands, 8)(brand_input)
    platform_emb = tf.keras.layers.Embedding(num_platforms, 8)(platform_input)

    user_vec = tf.keras.layers.Flatten()(user_emb)
    item_vec = tf.keras.layers.Flatten()(item_emb)
    brand_vec = tf.keras.layers.Flatten()(brand_emb)
    platform_vec = tf.keras.layers.Flatten()(platform_emb)

    dot_ui = tf.keras.layers.Dot(axes=1)([user_vec, item_vec])

    x = tf.keras.layers.Concatenate()([
        user_vec,
        item_vec,
        brand_vec,
        platform_vec,
        numeric_input,
        dot_ui
    ])

    x = tf.keras.layers.Dense(128, activation="relu")(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.30)(x)

    x = tf.keras.layers.Dense(64, activation="relu")(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.25)(x)

    x = tf.keras.layers.Dense(32, activation="relu")(x)
    output = tf.keras.layers.Dense(1, activation="sigmoid")(x)

    model = tf.keras.Model(
        inputs=[user_input, item_input, brand_input, platform_input, numeric_input],
        outputs=output
    )

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
        loss="binary_crossentropy",
        metrics=[
            tf.keras.metrics.BinaryAccuracy(name="accuracy"),
            tf.keras.metrics.AUC(name="auc")
        ]
    )

    return model


