def build_training_table(products_df, users_df, inter_df):
    data = inter_df.merge(products_df, on="item_index", how="left")
    data = data.merge(users_df, on="user_id", how="left")
    data = data.dropna(subset=["name"]).reset_index(drop=True)
    return data


def encode_features(data_df):
    df = data_df.copy()

    user_encoder = LabelEncoder()
    item_encoder = LabelEncoder()
    brand_encoder = LabelEncoder()
    platform_encoder = LabelEncoder()

    df["user_encoded"] = user_encoder.fit_transform(df["user_id"])
    df["item_encoded"] = item_encoder.fit_transform(df["item_index"])
    df["brand_encoded"] = brand_encoder.fit_transform(df["brand"].astype(str))
    df["platform_encoded"] = platform_encoder.fit_transform(df["platform"].astype(str))

    numeric_cols = ["price", "rating", "review_count", "discount_value"]
    scaler = MinMaxScaler()
    df[numeric_cols] = scaler.fit_transform(df[numeric_cols])

    meta = {
        "user_encoder": user_encoder,
        "item_encoder": item_encoder,
        "brand_encoder": brand_encoder,
        "platform_encoder": platform_encoder,
        "scaler": scaler,
        "numeric_cols": numeric_cols
    }

    return df, meta


def make_model_inputs(df):
    return {
        "user_input": df["user_encoded"].values,
        "item_input": df["item_encoded"].values,
        "brand_input": df["brand_encoded"].values,
        "platform_input": df["platform_encoded"].values,
        "numeric_input": df[["price", "rating", "review_count", "discount_value"]].values.astype("float32")
    }


