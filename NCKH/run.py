def preprocess_products(df):
    df = df.copy()
    df.columns = df.columns.str.strip()

    required_cols = [
        "platform", "keyword", "name", "brand", "price", "original_price",
        "discount", "rating", "review_count", "seller", "location",
        "item_id", "product_url", "image_url"
    ]

    for col in required_cols:
        if col not in df.columns:
            df[col] = np.nan

    # Tạo item_index đúng theo vị trí dòng
    df = df.reset_index().rename(columns={"index": "item_index"})

    # Làm sạch văn bản và lọc bỏ sản phẩm rác (tên quá ngắn hoặc là "0")
    df = df[df['name'].apply(lambda x: len(str(x)) > 5)].copy()
    text_cols = ["platform", "keyword", "name", "brand", "seller", "location"]
    for col in text_cols:
        df[col] = df[col].fillna("").apply(clean_text)

    # CHỈNH SỬA CHÍNH: Xử lý số liệu thực tế
    df["price"] = clean_currency_to_numeric(df["price"])
    df["original_price"] = clean_currency_to_numeric(df["original_price"])
    
    # Xử lý Rating (Tách số từ chuỗi "4.5/5")
    df["rating"] = df["rating"].astype(str).str.extract(r'(\d+\.?\d*)').astype(float).fillna(0)
    df["review_count"] = clean_currency_to_numeric(df["review_count"])
    df["discount_value"] = df["discount"].apply(parse_discount)

    # Logic xử lý giá 0.0
    missing_price_mask = df["price"] <= 0
    df.loc[missing_price_mask, "price"] = df.loc[missing_price_mask, "original_price"]
    
    # Điền giá trung vị nếu vẫn còn giá bằng 0 (Median Imputation)
    overall_median = df[df['price'] > 0]['price'].median()
    # Kiểm tra xem có cột brand không để tính median theo brand
    brand_median = df[df['price'] > 0].groupby('brand')['price'].transform('median')
    df.loc[df['price'] == 0, 'price'] = brand_median.fillna(overall_median)

    for col in ["name", "brand", "platform", "seller", "location", "product_url", "image_url"]:
        df[col] = df[col].fillna("")

    return df
