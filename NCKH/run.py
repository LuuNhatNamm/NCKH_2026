@st.cache_resource(show_spinner=False)
def train_model_cached(products_path, users_path, interactions_path, epochs, batch_size, embed_dim, eval_k):
    products_df = load_csv(products_path)
    users_df = load_csv(users_path)
    inter_df = load_csv(interactions_path)

    products_df = preprocess_products(products_df)
    users_df = preprocess_users(users_df)
    inter_df = preprocess_interactions(inter_df)

    data = build_training_table(products_df, users_df, inter_df)
    data_encoded, meta = encode_features(data)

    train_df, test_df = train_test_split(
        data_encoded,
        test_size=0.2,
        random_state=SEED,
        stratify=data_encoded["interaction"]
    )

    model = build_model(
        num_users=data_encoded["user_encoded"].nunique(),
        num_items=data_encoded["item_encoded"].nunique(),
        num_brands=data_encoded["brand_encoded"].nunique(),
        num_platforms=data_encoded["platform_encoded"].nunique(),
        embed_dim=embed_dim
    )

    train_inputs = make_model_inputs(train_df)
    test_inputs = make_model_inputs(test_df)

    callbacks = [
        tf.keras.callbacks.EarlyStopping(
            monitor="val_auc",
            mode="max",
            patience=3,
            restore_best_weights=True
        )
    ]

    history = model.fit(
        train_inputs,
        train_df["interaction"].values,
        validation_data=(test_inputs, test_df["interaction"].values),
        epochs=epochs,
        batch_size=batch_size,
        verbose=0,
        callbacks=callbacks
    )

    preds = model.predict(test_inputs, verbose=0).ravel()
    pred_labels = (preds >= 0.5).astype(int)

    metrics = {
        "test_auc": float(roc_auc_score(test_df["interaction"].values, preds)),
        "test_accuracy": float(accuracy_score(test_df["interaction"].values, pred_labels)),
        "train_rows": int(len(train_df)),
        "test_rows": int(len(test_df)),
    }

    topk_metrics = evaluate_topk(
        artifacts={
            "model": model,
            "products_df": products_df,
            "inter_df": inter_df,
            "meta": meta
        },
        test_df=test_df,
        k=eval_k,
        max_users=100
    )

    metrics.update(topk_metrics)

    artifacts = {
        "model": model,
        "products_df": products_df,
        "users_df": users_df,
        "inter_df": inter_df,
        "data_encoded": data_encoded,
        "train_df": train_df,
        "test_df": test_df,
        "meta": meta,
        "history": history.history,
        "metrics": metrics
    }

    return artifacts


